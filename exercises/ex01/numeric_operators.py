left_hand: int = int(input("Left-hand side: "))
right_hand: int = int(input("Right-hand side: "))
print(str(left_hand) + " ** " + str(right_hand) + " is " + str(left_hand ** right_hand))
print(str(left_hand) + " / " + str(right_hand) + " is " + str(left_hand / right_hand))
print(str(left_hand) + " // " + str(right_hand) + " is " + str(left_hand // right_hand))
print(str(left_hand) + " % " + str(right_hand) + " is " + str(left_hand % right_hand))